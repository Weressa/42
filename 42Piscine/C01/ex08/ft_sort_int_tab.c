void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	c;

	i = 0;
	while (i < size)
	{
		if (tab[i] > tab[i + 1])
		{
			c = tab[i + 1];
			tab[i + 1] = tab[i];
			tab[i] = c;
			i = 0;
		}
		else
			i++;
	}
}
