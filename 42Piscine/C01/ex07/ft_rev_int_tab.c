void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	n;

	i = 0;
	j = size - 1;
	while (i < j)
	{
		n = tab[i];
		tab[i] = tab[j];
		tab[j] = n;
		i++;
		j--;
	}
}
