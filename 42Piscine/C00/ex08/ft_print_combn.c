#include <unistd.h>

void	ft_print_combn(int n)
{
	char	c[n];
	char	i;

	
	c[0] = '0';
	i = 0;
	while (i <= n)
	{
		c[i + 1] = (i + 1) + 48;
		write (1, c[i], 1);
	}	
}

int	main(void)
{
	ft_print_combn(5);
	return (0);
}
