#include "push_swap.h"

void	sort3(t_stack **a)
{
	while (!sorted(*a))
	{
		if (((*a)->value) > (last_node(*a)->value))
			ra(a);
		else if (((*a)->value) > ((*a)->next->value))
			sa(*a);
		else 
		{
			ra(a);
		}
	}	
}
